code = {'0001101':0,
        '0011001':1,
        '0010011':2,
        '0111101':3,
        '0100011':4,
        '0110001':5,
        '0101111':6,
        '0111011':7,
        '0110111':8,
        '0001011':9}

T = int(input())
for t in range(1, T+1):
    candidate = set()
    n, m = map(int, input().split())
    for i in range(n):
        row = input()
        first = True  # 처음만 기록
        for j in range(m):
            if first and row[j] == '1':
                candidate.add(row[j-3:j-3+56])
                candidate.add(row[j-2:j-2+56])
                candidate.add(row[j-1:j-1+56])
                first = False

    candidate = list(candidate)

    answer = []  # [7, 5, 7, 5, 5, 0, 2, 7]
    for candi in candidate:
        decode = []  # 후보별 디코딩
        for idx in range(0, len(candi), 7):
            if candi[idx:idx+7] not in code:
                continue
            else:
                decode.append(code[candi[idx:idx+7]])
        if decode:
            answer = decode

    odd = 0
    even = 0
    for adx in range(len(answer)):  # 0-idx
        if adx % 2 == 0:  # 실제로는 홀수
            odd += answer[adx]
        else:
            even += answer[adx]

    result = odd*3+even
    print(f'#{t} ' + str(sum(answer)) if result % 10 == 0 else f'#{t} 0')